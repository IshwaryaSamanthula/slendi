package coreexample;

public class ConditionalStatements {

	public static void main(String[] args) {
	String genderCode="f";
	String genderDesc="";
	if(genderCode=="m") {
		genderDesc="male";
	}else {
		genderDesc="Female";
	}
	System.out.println(genderCode+";"+genderDesc);
		
	}
	

	}


